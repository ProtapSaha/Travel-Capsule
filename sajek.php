 <h3><pre>
 সাজেকে কোথায় থাকবেনঃ সাজেকে থাকার জন্য অনেক রিসোর্ট, কটেজ ও আদিবাসিদের বাড়িতে থাকা যায়। সাজেকে থাকার জন্য অবশ্যই আগে বুকিং দিয়ে যাবেন। কিন্তু অফ সিজনে সাজেক যেয়েও রুম বুকিং দেওয়া যায়।
ভাল ভিউ পাওয়া যায় এমন কয়েকটা রিসোর্ট হল , মেঘ মাচাং,মেঘ পুঞ্জি , জুম ঘর, এছাড়া সেনাবাহিনীর কিছু ভাল মানের কটেজ আছে সাজেকে।
সাজেকে ১৫০০ -৩০০০ টাকায় ভাল কটেজ পাওয়া যায়।
</pre>
 </h3><br><br>
 <pre>
সাজেকে কোথায় খাবেনঃ 
সাজেকে খাওয়ার জন্য অবশ্যই আগে অর্ডার করতে হয়।খাবারের মিনিমাম ১ ঘ্নটা আগে অর্ডার করুন।
সাজেকে প্রতিবেলা খাবারের বিভিন্ন প্যাকেজ সিস্টেম পাওয়া যায়।
১. ভাত+ডাল+আলুভর্তা+সবজি+দেশি মুরগী দাম ২০০ টাকা
২. ভাত+ডাল+আলুভর্তা+সবজি+ফার্মের মুরগী দাম ১৫০ টাকা
৩.ভাত+ডাল+আলুভর্তা দাম ১০০ টাকা।
রাতে ব্যাম্বু চিকেন অথবা বারবিকিউ খেতে পারেন দাম পড়বে ২২০ থেকে ৩০০ টাকা।

<h3>রুট - ০২ঃ ঢাকা থেকে মেইল ট্রেনে ফেনী তারপর শান্তি পরিবহনের বাসে খাগড়াছড়ি । ফেনী থেকে বাস ছাড়ে ৮ঃ৩০ সকাল ।তারপর খাগড়াছড়ি থেকে চান্দের গাড়িতে সাজেক। </h3>
<h3>রুট-০৩ঃ চট্রগ্রাম থেকে বাসে খাগড়াছড়ি ।দুই ধরণের বাস চলে 
১/ শান্তি পরিবহণের সিটিং সার্ভিস ভাড়া দিঘীনালা পর্যন্ত ২৪০ টাকা।
২/ লোকাল বাস - ভাড়া = ১৫০ টাকা।
সাজেকে চান্দের গাড়ি ২ টা ইস্কটে ছেড়ে যায়।প্রথম ইস্কট সকাল ১০ঃ৩০ এ , অন্যটা বিকাল ৩ঃ৩০ এ ছেড়ে যায়।
 </h3><br>
 <h3>
 	এবার জেনে নেওয়া যাক সাজেক সম্পর্কেঃ
সাজেক ভ্যালি রাঙ্গামাটি জেলার সর্বউত্তরের মিজোরাম সীমান্তে অবস্থিত। সাজেক হলো বাংলাদেশের সবচেয়ে বড় ইউনিয়ন, যার আয়তন ৭০২ বর্গমাইল। সাজেক এর উত্তরে ভারতের ত্রিপুরা, দক্ষিনে রাঙামাটির লংগদু, পূর্বে ভারতের মিজোরাম, পশ্চিমে খাগড়াছড়ির দীঘিনালা। সাজেক রাঙামাটি জেলায় অবস্থিত হলেও এর যাতায়াত সুবিধা খাগড়াছড়ি এর দীঘিনালা থেকে। রাঙামাটি থেকে নৌপথে কাপ্তাই হয়ে এসে অনেক পথ হেঁটে সাজেক আসা যায়। খাগড়াছড়ি জেলা সদর থেকে এর দূরত্ব ৭০ কিলোমিটার। আর দীঘিনালা থেকে ৪৯ কিলোমিটার। বাঘাইহাট থেকে ৩৪ কিলোমিটার।

 </h3>
 <h3>
 	খাগড়াছড়ি থেকে দীঘিনালা আর্মি ক্যাম্প হয়ে সাজেক যেতে হয়। পরে পরবে ১০ নং বাঘাইহাট পুলিশ ও আর্মি ক্যাম্প। যেখান থেকে আপনাকে সাজেক যাবার মূল অনুমতি নিতে হবে। তারপর কাসালং ব্রিজ, ২টি নদী মিলে কাসালং নদী হয়েছে। পরে টাইগার টিলা আর্মি পোস্ট ও মাসালং বাজার। বাজার পার হলে পরবে সাজেকের প্রথম গ্রাম রুইলুই পাড়া যার উচ্চতা ১৮০০ ফুট। এর প্রবীণ জনগোষ্ঠী লুসাই। এছাড়া পাংকুয়া ও ত্রিপুরারাও বাস করে। ১৮৮৫ সালে এই পাড়া প্রতিষ্ঠিত হয়। এর হেড ম্যান লাল থাংগা লুসাই। রুইলুই পাড়া থেকে অল্প সময়ে পৌঁছে যাবেন সাজেক। সাজেক এর বিজিবি ক্যাম্প বাংলাদেশের সর্বোচ্চ বিজিবি ক্যাম্প। এখানে হেলিপ্যাড আছে।

সাজেকে রয়েছে ২ টি হ্যালিপ্যাড ।যেখান থেকে সূর্যাস্ত- সূর্যদয় দেখা যায়।
সাজেক (Sajek Valley) এর রুইলুই পাড়া থেকে দুই থেকে আড়াই ঘন্টার ট্রেকিং করে দেখে আসতে পারেন সুন্দর কমলক ঝর্ণাটি। কমলক ঝর্ণাটি অনেকের কাছে পিদাম তৈসা ঝর্ণা অথবা সিকাম তৈসা ঝর্ণা নামে পরিচিত।
সাজেক থেকে ফেরার সময় হাজাছড়া ঝর্ণা, দীঘিনালা ঝুলন্ত ব্রিজ ও দীঘিনালা বনবিহার দেখে আসতে পারেন।
খাগড়াছড়ি শহরের আশেপাশে আরও ঘুরে দেখতে পারেন , আলুটিলা গুহা, তারেং হ্যালিপ্যাড, রিসাং ঝর্না , Suspension Bridge, Khagrachhari - ঝুলন্ত সেতু।


<h3>কিভাবে যাবেন :
প্রথমে চট্টগ্রাম/ঢাকা/যে কোন প্রান্ত থেকে বাসে করে সুনামগঞ্জ এর রাতের বাস ধরবেন।চট্টগ্রাম থেকে ৭০০টাকা ভাড়া, আর ঢাকা থেকে ৫০০ টাকা (নন-এসি)। একদম সকালে সুনামগঞ্জ পৌছাবেন। ।</h3><br><br>
<h3>৫/১০ টাকা লোকাল অটো তে করে বৈঠাখালী ব্রিজ পার হবেন।ওখান থেকে ৬০/৭০ টাকা লোকাল লেগুনা তে করে তাহিরপুর চলে যান। ১.৩০ ঘন্টা লাগবে প্রায়। এবার আসুন মেইন কথায়।এতক্ষণ যা বললাম তা বেলা ১০/১১ টার আগেই করতে হবে। কেননা যত লেইট করবেন ঘাটে নৌকা তত কমতে থাকবে,এন্ড বোট ক্রাইসিস বাড়বে।
 </h3><br>
 <h3>
 	মাঝিকে বলবেন টাংগুয়ার হাওড়ে ওয়াচ টাওয়ার, হিজল বাগান হয়ে ঘুরাঘুরি করে ট্যাকেরঘাট পর্জন্ত রিজার্ভ।মাঝি যাই বলুক ওয়াচ টাওয়ার এ টাইম দিবেন,চাইলে শাওয়ার করে নিতে পারেন।হাওড়ে চড় উঠলে নেমে দেখতে পারেন।এসবে ১৫০০-২০০০ এর বেশি যাবে না, তবে আপনি যদি রাতটাও কাটাতে চান নৌকায়, তাহলে পরের দিন ১০-১২ টা পর্যন্ত রিজার্ভ করতে পারেন।খরচ ৩০০০-৩৫০০ টাকার বেশি হবে না, খরচ কমাতে চাইলে নৌকা একদম সকালেও ছাড়তে পারেন, একচুয়ালি ট্যাকেরঘাট পৌছালে মাঝির প্রয়োজন নেই। যারা সন্ধায় নৌকা ছাড়বেন তারা ট্যাকেরঘাট এ রেস্ট হাউসে কাটাতে পারেন।তবে সাজেস্ট করব ২৫০০ টাকায় ভাড়া করে মাঝিকে সকাল সকাল ছেড়ে দিন। 
 </h3><br>
 <h3>
 	সকালে উঠে যে কাওকে জিজ্ঞেস করে সুরুম এ গিয়ে হাত মুখ ধুয়ে নিন।অইটা আসলে ঝর্ণার পানি,স্বাদ যেন অন্য লেভেল এর।তারপর সোজা বামে যেতে থাকুন।পৌছে যাবেন লাকমাছড়া।মনে রাখবেন আপনি তখন বর্ডার থেকে কয়েক গজ দূরে মাত্র।জায়গাটি অনেকটা বিছানাকান্দি,জাফলং এর মত।তবে পানির স্রোত বেশি।কিন্তু ওভারল পানি আপনার হাটুর নিচে থাকবে।পানিতে এঞ্জয় করে আগের মত ঘাটে ফিরুন।হোটেলে ব্রেকফাস্ট সেরে নিন।তারপর ঘাটের কাওকে জিজ্ঞেস করেই নিলাদ্রী চলে যান, একদম কাছে।পানি আসলেই নীল এবং ক্রিস্টাল।সাতার না জানলে লাইফজ্যাকেট নিবেন মাস্ট।ঘন্টা দেড়েক পানিতে সাতার কাটতে পারেন।অন্য মাত্রার মজা পাবেন।পাশেই ট্যাকেরটিলা,২০-২৫ মিনিট টাইম কাটান।টাইম পেলে ট্যাকেরঘাটে ঘুরতে পারেন,অনেক কিছুই দেখার আছে।কথা বলতে পারেন ভারতীয় গারোদের সাথে। 
 </h3><br>
 <h3>
 	তারপর বাইক ভাড়া করতে হবে একেবারে বারিক্কাটিলা,জাদুকাটা নদী,সুনামগঞ্জ পর্যন্ত ।এক বাইকে ২ জন,২৫০ টাকা করে নিবে।চলে যাবেন,রাজাই হয়ে বারেক্কাটিলা।চাইলে পথে মনোরম স্থানগুলো তে থামতে পারেন,বিশেষ করে রাজাই তে।টিলায় পৌছে ২০-২৫ মিনিট সময় দিন।তারপর প্রতি বাইক ২০ টাকায় ট্রলার করে জাদুকাটা নদী পার হোন।আধঘন্টার মত জাদুকাটায় গোসল করতে পারেন। ওখান থেকে আবার আগের বাইকে সুনামগঞ্জ চলে যাবেন।

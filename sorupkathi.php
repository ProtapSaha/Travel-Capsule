<h3>কিভাবে যাবেন স্বরূপকাঠী :<br>
১ম দিনঃ ঢাকার যেখানেই থাকেন সোজা সদরঘাট চলে যান সন্ধ্যা ৬ টার মধ্যে।  ৫ নম্বর প্লাটুন থেকে ঢাকা-হুলারহাট - ভান্ডারিয়া রুটের যেকোন লঞ্চে উঠে পড়ুন। বিলাসবহুল কিছু লঞ্চের নাম- রাজদূত ৭, অগ্রদূত প্লাস, রণদূত প্লাস। ভাড়াঃ- ডেকঃ ২৫০-৩০০ টাকা। কেবিনঃ- ৯০০ টাকা (সিঙ্গেল), ১৮০০ টাকা (ডাবল)। সিংগেল কেবিন ১ জনের জন্য হলেও  ডেকের সমতূল্য ভাড়া দিয়ে যে কয়জন ইচ্ছা যেতে পারেন। ডাবল কেবিনের ক্ষেত্রেও একই কথা প্রযোজ্য।<br>
লঞ্চ ছাড়লে পড়ে লঞ্চের ছাদে চলে যান। রাতে নদীর সৌন্দর্য উপভোগ করুন। </h3><br>
<h3>২য় দিনঃ ভোর ৬ টার দিকে আপনাকে ইন্দুরহাট লঞ্চ টার্মিনালে নামিয়ে দিবে।<br>
  লঞ্চ থেকে নেমে কাউকে জিজ্ঞেস করুন মিয়ারহাট লঞ্চ টার্মিনাল কোথায়।  টার্মিনালে গিয়ে সারাদিনের জন্য বোট ভাড়া করুন। টাকা বেশী চাইবে। দরাদরি করলে ৫০০-৭০০ টাকার মধ্যে সকাল থেকে দুপুরের জন্য পেয়ে যাবেন। তবে বেশী দরাদরি না করাই ভালো। তাহলে মাঝি আপনাকে সব যায়গা ঘুরিয়ে দেখাতে নাও পারে।<br> মাঝিকে বলুন কুড়িয়ানা ঘুরিয়ে দেখাতে।
  যাওয়ার পথে দেখতে পাবেন ছবির মত স্বরূপকাঠীর মানুষের প্রধান ব্যাবসা গোলপাতা আর ভাসমান কাঠের হাট।  মাঝি আপনাকে সব যায়গায় ঘুরিয়েও দেখাবে। বাগান মালিকের অনুমতি নিয়ে বাগানে ঢুকে ঘুরতে পারেন।  (ভুলেও অনুমতি না নিয়ে ঢুকবেন না। ঝামেলা করবে তাহলে অনেক) । ভাসমান পেয়ারা বাজার থেকে ইচ্ছে করলে পেয়ারা কিনতে পারেন। দাম নিবে প্রতি কেজি ৮- ১০ টাকা। দুপুরের মধ্যে ফিরে আসেন।এবার খেয়ে কাউকে জিজ্ঞেস করলে কলেজ দিঘী দেখিয়ে দিবে। সেখানে কিছুক্ষন বিশ্রাম নিয়ে চলে যান সন্ধ্যা নদীর পাড়ে (যদি অই দিন ফেরার ইচ্ছা না থাকে)। নদীর পাড়ে সূর্যাস্ত উপভোগ করুন। রাতেও থাকতে পারেন কিছু সময়। নদী পার হয়ে ওপারে গিয়ে ঘুরে আসতে পারেন ছারছীনা দরবার শরীফ।<br>
  রাতের খাবার দুপুরের হোটেলে খেয়ে আবার মিয়ারহাট বাজারে চলে আসুন। কোনো হোটেলে উঠে পড়ুন। ৩০০ টাকার মধ্যে পেয়ে যাবেন। <br><br>

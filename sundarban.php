<h3>কিভাবে যাবেন সুন্দরবন :
সুন্দরবন যেতে হলে খুলনা বা মংলা এর যে কোন একটি যায়গা থেকে আপনাকে উঠতে হবে লঞ্চে। সেখানে রয়েছে বিভিন্ন অপারেটরের বিভিন্ন মাপ ও মানের লঞ্চ।<br>
খুলনা থেকে সাধারনত ৩০ থেকে ৭০ বেডের লঞ্চ পাওয়া যায়। আপনার টিমের আকার যদি ছোট হয় (দুই চার জন) সেগুলোতে অন্যদের সাথে কম্বাইন্ড করে সুন্দরবন ঘুরে আসতে পারেন। সাধারনত প্রতি শুক্র-রবি অথবা সোম/মঙ্গল-বুধ/বৃহঃ খুলনা লঞ্চঘাট থেকে অপারেটরদের প্যাকেজ থাকে। এছাড়া প্রতিদিনই প্যাকেজ থাকার সম্ভাবনা থাকে, কিন্তু নিশ্চিত নয়। আর নিজেদের লোক সংখ্যা যদি একটি লঞ্চের সমান হয় তাহলে প্যাকেজের ভিতরেই নিজেরাই একটি লঞ্চ নিতে পারবেন।<br>মংলা থেকেও এই প্যাকেজগুলো করা হয়, কিন্তু এক দুই জনের জন্য নিয়মিত কোন প্যাকেজ নেই। অনেকে সময় বাঁচানোর জন্য খুলনার লঞ্চ গুলোই মোংলায় এনে সেখান থেকে প্যাকেজ করে থাকে।মোংলা থেকে নতুন একটি সার্ভিস চালু হয়েছে। ১০ থেকে ২০ জনের গ্রুপ হলেও প্যাকেজে নিজেরা একটি লঞ্চ নিতে পারবেন। এতে আপনাদের প্রাইভেসিও থাকবে, লোক কম হওয়ায় সার্ভিসও ভালো পাবার সম্ভাবনা থাকে। আর মংলা থেকে লঞ্চে সময়ও ৩ ঘন্টা কম লাগে। </h3><br>
<h3>খরচ:
সুন্দরবনের প্যাকেজগুলোতে জাহাজে ওঠার পর থেকে তিনদিনের টুর শেষে আবার ঘাটে ফেরা পর্যন্ত লঞ্চভাড়া, ফুয়েল, খাবার, নাস্তা, প্রত্যেকের সরকারী পাশ, রেভিনিউ, গাইড, গানম্যান, বনে ঘুরার ছোট নৌকা সহ অন্যান্য সব খরচ ইনক্লুড থাকে। <br>
খাবারের মান ও জাহাজ ভেদে মাঝারি ধরনের একটি টুরে সাধারনত খরচ পরে ৬০০০-৮০০০ টাকা। বেশি ভালো যেতে চাইলে এর উপরে পারহেড ১৯,০০০ টাকা পর্যন্তও প্যাকেজ আছে।
তবে ন্যাশনাল হলিডে গুলোতে খরচের কোন লিমিট নেই। আর পরাপর দুইদিন যদি ছুটি পরে তাহলেতো কোন কথাই নেই। খরচ আকাশও ছুতে পারে। আর এডভান্স দিয়ে এক দেড় মাস আগে বুকিং না দিলে ভ্যাসেল পাওযাই কঠিন।

